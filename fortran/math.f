	MODULE MATH
	
	CONTAINS

		FUNCTION MATHSIGMA(I,N,X)

			INTEGER :: I,J,N,X,Y
			J = I
			
			DO WHILE (J .LT. N)
				J = J + X 
				Y = Y + J
			END DO
			
			IF (X .EQ. N) THEN
				MATHSIGMA = 1
			ELSE
				MATHSIGMA = Y + 1
			END IF

		END FUNCTION MATHSIGMA
		
		FUNCTION FACTORIAL(X)

			INTEGER :: I, X, FACT
			FACT = 1
			
			DO I = 1, X 
				FACT = FACT * I 
			END DO
			FACTORIAL = FACT

		END FUNCTION FACTORIAL
		
		SUBROUTINE CATALAN_NUMBERS(N)
			
			INTEGER :: I
			I = FACTORIAL(N * 2) / (FACTORIAL(N + 1) * FACTORIAL(N))
			PRINT *, I

		END SUBROUTINE CATALAN_NUMBERS

	END MODULE MATH
